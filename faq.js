// FONCTION PRINCIPALE
// ==============================
document.addEventListener("DOMContentLoaded", () => {
    const languageSwitcher = document.getElementById("language-switcher");

    function setLanguage(lang) {
        const t = translations[lang];
        if (!t) return;

        // Met à jour le titre de la page
        document.title = t.title;

       // Met à jour le H1
    const h1 = document.getElementById("faq_title");
    if (h1) h1.textContent = t.faq_title;

        // Container FAQ
        const container = document.querySelector(".faq-container");
        if (!container) return;
        container.innerHTML = ""; // vide le contenu existant

        // Injection des sections
        t.sections.forEach(section => {
            const div = document.createElement("div");
            div.className = "faq-item";

            const h3 = document.createElement("h3");
            h3.textContent = section.h3;
            const p = document.createElement("p");
            p.textContent = section.p;

            div.appendChild(h3);
            div.appendChild(p);
            container.appendChild(div);
        });

        // Footer
        const footer = document.querySelector("footer p");
        if (footer) footer.innerHTML = t.footer;

        // Sauvegarde de la langue
        localStorage.setItem("lang", lang);
    }

    // Détection de la langue sauvegardée ou du navigateur
    const savedLang = localStorage.getItem("lang") || (navigator.language || navigator.userLanguage).slice(0,2) || "fr";
    languageSwitcher.value = savedLang;
    setLanguage(savedLang);

    // Changement de langue via select
    languageSwitcher.addEventListener("change", (e) => {
        const lang = e.target.value;
        setLanguage(lang);
    });
});