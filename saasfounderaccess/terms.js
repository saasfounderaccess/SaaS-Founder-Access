document.addEventListener("DOMContentLoaded", () => {
    const languageSwitcher = document.getElementById("language-switcher");

    function setLanguage(lang) {
        const t = translations[lang];
        if (!t) return;

        document.title = t.title;

        const container = document.querySelector(".terms-container");
        container.innerHTML = ""; // vide le contenu

        t.sections.forEach(section => {
            const div = document.createElement("div");
            div.className = "terms-item";

            const h2 = document.createElement("h2");
            h2.textContent = section.h2;
            const p = document.createElement("p");
            p.textContent = section.p;

            div.appendChild(h2);
            div.appendChild(p);
            container.appendChild(div);
        });

        // Met à jour le H1 de la page
const headerH1 = document.querySelector("header h1");
if (headerH1) headerH1.textContent = t.h1;

        // Footer
        const footer = document.querySelector("footer p");
        if (footer) footer.innerHTML = t.footer;

        localStorage.setItem("lang", lang);
    }

    // Détecter langue sauvegardée ou navigateur
    const savedLang = localStorage.getItem("lang") || navigator.language.slice(0,2) || "fr";
    languageSwitcher.value = savedLang;
    setLanguage(savedLang);

    // Changement via select
    languageSwitcher.addEventListener("change", (e) => {
        const lang = e.target.value;
        setLanguage(lang);
    });
});